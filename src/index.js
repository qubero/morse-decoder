const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    const SIZE = 10,
          DOT = '.',
          DASH = '-';

    let result = '';

    for (let i = 0; i < expr.length; i += SIZE) {
        const symbol = expr.slice(i, i + SIZE);

        result += symbol.startsWith('*')
            ? ` `
            : MORSE_TABLE[String(+symbol).replace(/10/g, DOT).replace(/11/g, DASH)];
    }

    return result;
}

module.exports = {
    decode
}